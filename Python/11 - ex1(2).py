x = int(input("Coloque um numero de 1 a 7 e veja qual dia da semana é: "))

if x == 1:
    print("Domingo")
elif x == 2:
    print("Segunda")   
elif x == 3:
    print("Terça")   
elif x == 4:
    print("Quarta")    
elif x == 5:
    print("Quinta")   
elif x == 6:
    print("Sexta")   
elif x == 7:
    print("Sabado")
else:
    print("Dia inexistente")

