x = int(input("1 - domingo, 2 - segunda, 3 - terça, 4 - quarta, 5 - quinta, 6 - sexta, 7 - sabado: "))
if x == 1:
    print("Domingo")
elif x == 2:
    print("Segunda")
elif x == 3:
    print("Terça")
elif x == 4:
    print("Quarta")
elif x == 5:
    print("Quinta")
elif x == 6:
    print("Sexta")
elif x == 7:
    print("Sabado")
else:
    print("Numero invalido")
    
